export default function classificacaoIndicativaCor(numero) {
	switch (numero) {
		case 0:
			return "bg-green-500";
		case 10:
			return "bg-blue-400";
		case 11:
			return "bg-blue-400";
		case 12:
			return "bg-amber-400";
		case 13:
			return "bg-amber-400";
		case 14:
			return "bg-orange-600";
		case 15:
			return "bg-orange-600";
		case 16:
			return "bg-red-600";
		case 17:
			return "bg-red-600";
		case 18:
			return "bg-black";
	}
}
